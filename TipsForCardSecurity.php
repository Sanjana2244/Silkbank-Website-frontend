                    <h2 class="Top-heading">Tips for Card Security</h2>
                    <p class="Paragraph mt-4">

                        Using your credit card to pay for things is a safer and convenient way to shop, but there are a
                        few things you can do to safeguard your details. Fraudsters will stop at nothing to get your
                        personal information and card data. Their scams can be clever, but not clever enough, if you
                        know how they work and how to avoid them. Here are some tips on how you can keep your personal
                        and card information safe and out of the hands of fraudsters.</p>

                    <h3 class="mt-4 mb-3 center-heading">Using Your Card Securely</h3>
                    <ul class="list-group ">
                        <li class="list-item"> Always know where your card is at all times</li>
                        <li class="list-item"> Never write down your Personal Identification Number (PIN)-memorize it
                        </li>
                        <li class="list-item"> Never disclose your PIN to anyone</li>
                        <li class="list-item"> Do not choose numbers and letters that can be easily identified when
                            selecting
                            a PIN (e.g.
                            avoid using initials, phone numbers, or birth dates)</li>
                        <li class="list-item"> Ensure you sign your card on the signature panel as soon you receive it
                        </li>
                        <li class="list-item"> Make a record of card account numbers and telephone numbers for reporting
                            lost
                            or stolen
                            cards and keep this list in a safe place</li>
                    </ul>
                    <h3 class="mt-4 mb-3 center-heading">Retail and ATM</h3>
                    <P class="Paragraph">Credit Cards are accepted across the world and are more convenient & safer than
                        cash. Payment
                        cards have transformed how we shop and bank. The Chip has had a significant impact on securing
                        face to face transactions and in reducing counterfeit fraud. But fraudsters may try to steal
                        your card information and use it for unauthorized charges. Make sure you make these transactions
                        in ways that reduce your risk of fraud.</P>
                    <ul class="list-group">

                        <li class="list-item"> Be aware of your surroundings</li>
                        <li class="list-item">Review receipts before you sign</li>
                        <li class="list-item">When entering your PIN at the ATM, always make sure to cover the key pad
                            using your free
                            hand</li>
                        <li class="list-item">Guard your PIN from fraudster “shoulder surfing”</li>
                        <li class="list-item"> Subscribe to Silkbank Credit Card SMS & E-Alerts</li>
                        <li class="list-item">Keep copies of ATM and sales receipts for your records  and regularly
                            monitor your accounts
                            for any unusual card activity</li>
                    </ul>
                    <h3 class="mt-4 mb-3 center-heading">Online</h3>
                    <P class="Paragraph">From spyware to dodgy merchants, the threat of online fraud is real, but you
                        are the best line of defense. The key to combating online fraud is knowing what threats exist
                        and taking easy steps to beat them.</P>
                    <ul class="list-group">

                        <li class="list-item"> Silkbank Visa Credit Cards are powered by Verified by Visa (VBV) feature
                            for added security
                        </li>
                        <li class="list-item">Keep your software and virus protection up to date on your computer and
                            devices</li>
                        <li class="list-item">Create strong passwords</li>
                        <li class="list-item">Ignore emails from senders you don’t know</li>
                        <li class="list-item"> Download files only from sites you know</li>
                        <li class="list-item"> Subscribe to Silkbank Credit Card SMS & E-Alerts to keep track of your
                            purchases</li>
                    </ul>
                    <h3 class="mt-4 mb-3 center-heading">Mobile</h3>
                    <P class="Paragraph">Mobile technology is everywhere. It is changing how we communicate, how we do
                        business, and how we access our personal and professional lives. But we need to remind ourselves
                        to stay safe when using our mobiles.</P>
                    <ul class="list-group">

                        <li class="list-item">Protect the devices you use both physically and with passwords/PIN codes
                        </li>
                        <li class="list-item">Sensitive account information should never be stored on your mobile device
                        </li>
                        <li class="list-item">Be aware of your surroundings—don’t loudly read your details in a public
                            environment</li>
                    </ul>
                    <h3 class="mt-4 mb-3 center-heading">Email</h3>
                    <P class="Paragraph">Email phishing scams try to trick you into revealing credit card numbers, PIN,
                        bank account passwords or other private information. Most phishing starts as an email that links
                        to a fake Internet site that looks like the real thing with familiar logos and graphics, but it
                        is not. When you enter personal information on such sites, it gets sent to fraudsters.
                    </P>
                    <ul class="list-group">

                        <li class="list-item">Consider all email requests for personal information to be suspicious
                        </li>
                        <li class="list-item">Silkbank never calls or writes cardholders for personal account
                            information</li>
                        <li class="list-item">Do not respond to such emails or enter information on questionable
                            websites</li>
                        <li class="list-item">Check the legitimacy of the enquiry by contacting the number on the back
                            of your card</li>
                        <li class="list-item">Report suspicious emails claiming to be from Silkbank at 021-111-100-777
                        </li>
                    </ul>
                    <h3 class="mt-4 mb-3 center-heading">Using your card whilst abroad</h3>
                    <ul class="list-group">

                        <li class="list-item">Avoid leaving cards unattended, in a hotel room, public areas, or in a
                            locked or unlocked
                            vehicle
                        </li>
                        <li class="list-item">Take advantage of the safe or security box provided by the hotel for your
                            valuables</li>
                        <li class="list-item">Save all of your receipts for proof of purchase. And when you get home,
                            carefully check them
                            against your monthly
                            statements</li>

                    </ul>
                    <h3 class="mt-4 mb-3 center-heading">And in case your card is lost or stolen</h3>
                    <P class="Paragraph">Immediately contact Silkbank helpline at 021-111-100-777 if your card gets
                        lost, stolen, retained by an ATM, or you suspect that someone might have obtained your PIN.
                        Also, sign up for SilkMobile app whereby you can use our unique “Guard your Card” feature to
                        block / unblock your credit card.
                    </P>
